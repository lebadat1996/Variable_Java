public class variable {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        float f = 10.2f;
        System.out.println(f);
        double d = 20.5;
        System.out.println(d);
        boolean b = true;
        System.out.println(b);
        char c = 'a';
        System.out.println(c);
        String s = "Hà Nội";
        System.out.println(s);
    }
}
